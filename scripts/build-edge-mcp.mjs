// Pre-bundle the hosted-MCP Edge Function for Deno.
//
// The function's source (`server-entry.ts`) reuses the Node-style shared modules
// (`mcp/*`, `src/lib/measure/*`) which use extensionless imports, the `@data` tsconfig
// alias and attribute-less JSON imports — none of which the Supabase (Deno) bundler
// accepts. esbuild resolves all of that here (extensions, alias, JSON inlined, types
// erased) and emits a single `index.ts` whose only remaining imports are the npm
// packages, which `deno.json`'s import map maps to `npm:` specifiers at deploy.
//
//   node scripts/build-edge-mcp.mjs   (or: npm run build-edge)
// Re-run after changing any module the function pulls in, then `supabase functions
// deploy mcp`.
import { build } from 'esbuild';
import { writeFileSync } from 'node:fs';

const OUT = 'supabase/functions/mcp/index.ts';

const result = await build({
  entryPoints: ['supabase/functions/mcp/server-entry.ts'],
  bundle: true,
  format: 'esm',
  platform: 'neutral',
  target: 'esnext',
  // Keep the npm packages external — Deno resolves them via the import map. Everything
  // else (the local TS graph + JSON authority data) is inlined.
  external: ['@modelcontextprotocol/sdk/*', '@supabase/supabase-js', 'zod', 'zod/*', 'zod-to-json-schema', 'node:*'],
  // The `@data` / `@` aliases appear only in type-only imports (erased), but map them
  // so a stray value import would still resolve rather than break the bundle.
  alias: { '@data': './data', '@': './src' },
  loader: { '.json': 'json' },
  write: false,
  logLevel: 'info',
});

const banner = `// GENERATED by scripts/build-edge-mcp.mjs — DO NOT EDIT.
// Source: supabase/functions/mcp/server-entry.ts (+ the shared mcp/ and src/lib/measure/ graph).
// Regenerate with \`npm run build-edge\`, then \`supabase functions deploy mcp --no-verify-jwt --use-api\`.
`;
writeFileSync(OUT, banner + result.outputFiles[0].text);
console.log(`\nwrote ${OUT} (${(result.outputFiles[0].text.length / 1024).toFixed(1)} kB)`);
